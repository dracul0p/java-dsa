// logical operator : && , || , !  
// &&: both condition to be true  for statment to be true
// ||: any one condition to be true for statment to be true
public class operator5 {
    public static void main(String args[]) {
        System.out.println(  (3>2) && (5>0)  );
        System.out.println(  (3>2) && (5>9)  );
        System.out.println(  (3>9) || (5>9)  );
        System.out.println(  (3>2) || (5<6)  );
        System.out.println(  ! (5>9)  );
        System.out.println(  ! (5<9)  );
        
        
    }
}
