// assignnment operator: =  +=  -=  *=   /=
public class operator6 {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        // a = a+10;
        a += 10;
        System.out.println(a);
        int b =20;
        // b-=5;    // b = b+5
        b-=5;
        System.out.println(b );
        int d = 6;
        d *=6;  // d = d *6
        System.out.println(d);

        d /=6;
        System.out.println(d);

    }
    
}
